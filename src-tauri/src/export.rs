//! specta → TypeScript bindings exporter (T003).
//!
//! Rust DTOs are the single source of truth. This module collects the wire types
//! and renders `packages/shared/src/bindings.ts`. It is compiled ONLY under the
//! `specta-export` feature (the `gen-bindings` bin), so the shipping app never
//! links `specta-typescript`.
//!
//! `pnpm gen:types` runs the `gen-bindings` bin; CI then runs it and
//! `git diff --exit-code packages/shared/src/bindings.ts` so a changed Rust type
//! that wasn't regenerated fails the build (08 §6, 02 §6).
//!
//! NOTE: specta v2 / specta-typescript are pre-1.0 (rc). If the resolved patch
//! version changes the collection/export entry points, this single function is
//! the only place to adjust — the frontend depends on the committed `bindings.ts`,
//! not on this code path.

