/// Registration shim over rc.20's `TypeMap` (rc.20 has no `TypeCollection`).
/// `definition_named_data_type` walks the type, pulling every referenced named
/// type into the shared map; inserting under `T::sid()` makes the registered
/// type itself part of the export set (generic types like `PageResult<T>` are
/// stored once, as their generic definition, regardless of the instantiation
/// used to register them).
struct TypeRegistry {
    map: TypeMap,
}

impl TypeRegistry {
    fn register<T: NamedType>(&mut self) {
        let ndt = T::definition_named_data_type(&mut self.map);
        self.map.insert(T::sid(), ndt);
    }
}

